public class Main {

    static void sum (int a, int b) {

        System.out.println("The summ of a and b is: " + a + b);
    }

    static void prod (int a, int b) {

        System.out.println("The product of a and b is: " + a * b);
    }
    
    public static void main(String[] args) {

        sum(10,5);
        prod(10,5);
        
    }
}
